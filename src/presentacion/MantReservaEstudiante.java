    public void fillStudents() {
        List<Estudiante> list = getEbo().getAll();
        this.jComboBoxEstudiante.addItem("---Seleccione estudiante---");
        for (Estudiante e : list) {
            this.jComboBoxEstudiante.addItem(e.getCedula() + "-" + e.getNombre());
        }
    }

    public void fillBooks() {
        List<Libro> list = getLbo().getAll();
        this.jComboBoxLibro.addItem("---Seleccione libro---");
        for (Libro lb : list) {
            this.jComboBoxLibro.addItem(lb.getCodigo() + "-" + lb.getNombre());
        }
    }


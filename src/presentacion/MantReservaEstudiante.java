    private boolean validateData() {
        if (jComboBoxEstudiante.getSelectedIndex() == 0) {
            showJOptionPane("Seleccione un profesor");
            return false;
        }
        if (jComboBoxLibro.getSelectedIndex() == 0) {
            showJOptionPane("Seleccione un libro");
            return false;
        }
        return true;
    }

    private void showJOptionPane(String string) {
        JOptionPane.showMessageDialog(null, string);
    }


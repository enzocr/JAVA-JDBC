    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (validateData()) {
            ReservaEstudiante ire = new ReservaEstudiante();

            String profe = this.jComboBoxEstudiante.getSelectedItem().toString();
            String[] partesProfe = profe.trim().split("-");
            ire.setCedulaEstudiante(Integer.parseInt(partesProfe[0]));

            String libro = this.jComboBoxLibro.getSelectedItem().toString();
            String[] partesLibro = libro.trim().split("-");
            ire.setCodigoLibro(Integer.parseInt(partesLibro[0]));

            ire.setDiasPrestamo((Integer) spDias.getValue());
            ire.setFecha(new Date());

            getRebo().insert(ire);

        }
    }//GEN-LAST:event_addButtonActionPerformed


    private void tableEstudianteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEstudianteMouseClicked

        if (evt.getClickCount() == 2) {
            int row = tableEstudiante.getSelectedRow();
            cedulaTextField.setText(getTableEstudiante().getValueAt(row, 0).toString());
            nombreTextField.setText(getTableEstudiante().getValueAt(row, 1).toString());
            carreraTextField.setText(getTableEstudiante().getValueAt(row, 2).toString());
            carnetTextField.setText(getTableEstudiante().getValueAt(row, 3).toString());
            this.cedulaTextField.setEnabled(false);
            this.carnetTextField.setEnabled(false);
        }
    }//GEN-LAST:event_tableEstudianteMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (validateData()) {

            Estudiante est = new Estudiante(
                    Integer.parseInt(this.cedulaTextField.getText()),
                    this.nombreTextField.getText(),
                    this.carreraTextField.getText());
            est.setCarnet(Integer.parseInt(this.carnetTextField.getText()));

            int res = getEstBo().update(est);

            switch (res) {
                case 0:
                    ValidationUtil.showJOptionPane("Estudiante modificado");
                    break;
                case 1:
                    ValidationUtil.showJOptionPane("No se pudo conectar a la BD");
                    break;
                case 2:
                    ValidationUtil.showJOptionPane("Ya existe estudiante con esta cedula");
                    break;
                case 3:
                    ValidationUtil.showJOptionPane("Error inesperado");
                    break;
            }

        }
        cleanTextFields();

    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (cedulaTextField.getText().trim().equals("")) {
            ValidationUtil.showJOptionPane("Ingresar cédula a eliminar");
        } else {
            try {
                int cedula = Integer.parseInt(cedulaTextField.getText());
                Estudiante est = new Estudiante();
                est.setCedula(cedula);
                int res = getEstBo().delete(est);

                switch (res) {
                    case 0:
                        ValidationUtil.showJOptionPane("Estudiante NO eliminado");
                        break;
                    case 1:
                        ValidationUtil.showJOptionPane("Estudiante ELIMINADO");
                        break;
                    case 2:
                        ValidationUtil.showJOptionPane("NO hubo conexion con base de datos");
                        break;
                    case 3:
                        ValidationUtil.showJOptionPane("Error inesperado");
                        break;
                    case 4:
                        ValidationUtil.showJOptionPane("Estudiante tiene registros asociados");
                }
                cleanTextFields();

            } catch (NumberFormatException nfe) {
                ValidationUtil.showJOptionPane("Solo se permiten numeros");
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void getByIdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getByIdButtonActionPerformed
        if (!this.cedulaTextField.getText().matches("[0-9]+")) {
            ValidationUtil.showJOptionPane("NO se permiten letras");
        } else {
            int cedula = Integer.parseInt(this.cedulaTextField.getText());
            Estudiante est = getEstBo().getById(cedula);
            List<Estudiante> list = new ArrayList<>();
            list.add(est);
            fillTable(list);
        }
    }//GEN-LAST:event_getByIdButtonActionPerformed

    private void getByNameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getByNameButtonActionPerformed
        if (!"".equals(this.nombreTextField.getText())) {
            List<Estudiante> list = getEstBo().getByName(this.nombreTextField.getText());
            fillTable(list);
        } else {
            ValidationUtil.showJOptionPane("Digitar nombre para búsqueda");
        }


    }//GEN-LAST:event_getByNameButtonActionPerformed
    private void cleanTextFields() {

        this.carnetTextField.setEditable(false);
        this.cedulaTextField.setText("");
        this.nombreTextField.setText("");
        this.carreraTextField.setText("");
        this.carnetTextField.setText("");
        fillTable();
    }


    private boolean validateData() {

        this.carnetTextField.setEditable(false);
        if (this.cedulaTextField.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite cedula del estudiante");
            return false;
        }
        if (this.nombreTextField.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite nombre del estudiante");
            return false;
        }
        if (this.carreraTextField.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite carrera del estudiante");
            return false;
        }

        return true;
    }

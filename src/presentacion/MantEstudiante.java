    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        this.carnetTextField.setEditable(false);
        if (validateData()) {

            Estudiante e = new Estudiante(
                    Integer.parseInt(this.cedulaTextField.getText()),
                    this.nombreTextField.getText(),
                    this.carreraTextField.getText());

            int res = getEstBo().insert(e);

            switch (res) {
                case 0:
                    JOptionPane.showMessageDialog(null, "Estudiante registrado");
                    break;
                case 1:
                    JOptionPane.showMessageDialog(null, "No se pudo conectar a la BD");
                    break;
                case 2:
                    JOptionPane.showMessageDialog(null, "Ya existe estudiante con esta cedula");
                    break;
                case 3:
                    JOptionPane.showMessageDialog(null, "Error inesperado");
                    break;
            }

        }
        cleanTextFields();

    }//GEN-LAST:event_addButtonActionPerformed


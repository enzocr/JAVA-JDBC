    public void fillTable() {

        this.modelEstudiante = new DefaultTableModel() {
            private static final long serialVersionUID = 1L;

            @Override
            public boolean isCellEditable(int row, int colum) {
                return false;
            }
        };
        this.tableEstudiante.getTableHeader().setReorderingAllowed(false);

        getModelEstudiante().setColumnCount(0);
        getModelEstudiante().addColumn("Cédula");
        getModelEstudiante().addColumn("Nombre");
        getModelEstudiante().addColumn("Carrera");
        getModelEstudiante().addColumn("Carnet");

        List<Estudiante> list = getEstBo().getAll();

        if (list != null) {
            getModelEstudiante().setNumRows(list.size());

            for (int i = 0; i < list.size(); i++) {
                Estudiante est = list.get(i);
                getModelEstudiante().setValueAt(est.getCedula(), i, 0);//i=fila 0=columna
                getModelEstudiante().setValueAt(est.getNombre(), i, 1);
                getModelEstudiante().setValueAt(est.getCarrera(), i, 2);
                getModelEstudiante().setValueAt(est.getCarnet(), i, 3);
            }
        }

        this.tableEstudiante.setModel(getModelEstudiante());

    }

